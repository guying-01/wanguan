angular.module('hardware.routes',[])
  .config(function($stateProvider, $urlRouterProvider) {
    $stateProvider
      .state("tabs", {
        url: "/tabs",
        abstract:true,
        templateUrl:'template/tabs.html',
      })
      .state("tabs.homePage",{
        url:'/homePage',
        views:{
          'homePage-view':{
            templateUrl:'template/homePage.html',
            controller:'homePageCtrl'
          }
        }
      })
      .state("tabs.goodsList",{
        url:'/goodsList',
        views:{
          'goodsList-view':{
            templateUrl:'template/goodsList.html',
            controller:'goodsListCtrl'
          }
        }
      })
      .state("tabs.orderList",{
        url:'/orderList',
        views:{
          'orderList-view':{
            templateUrl:'template/orderList.html',
            controller:'orderListCtrl'
          }
        }
      })
      .state("tabs.personalCenter",{
        url:'/personalCenter',
        views:{
          'personalCenter-view':{
            templateUrl:'template/personalCenter.html',
            controller:'personalCenterCtrl'
          }
        }
      })
      .state('cart',{
        url:'/cart',
        templateUrl:'template/cart.html',
        controller:'cartCtrl'
      })
      .state('login',{
        url:'/login',
        templateUrl:'template/login.html',
        controller:'loginCtrl'
      })
      .state('pay',{
        url:'/pay',
        templateUrl:'template/pay.html',
        controller:'payCtrl'
      })
    $urlRouterProvider.otherwise("/login")
  })
